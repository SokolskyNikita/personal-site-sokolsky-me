export const AXIS_ORDER = AXES.map((axis) => axis.key);

export function fingerprintSegments(seed: Record<string, number>) {
  return AXIS_ORDER.map((key) => {
    const v = seed[key];
    const left = v >= 0 ? 50 : 50 + v * 50;
    const width = Math.abs(v) * 50;
    return { key, v, left, width };
  });
}
