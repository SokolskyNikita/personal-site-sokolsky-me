const EARTH_RADIUS_KM = 6371;

/** Great-circle distance in km (naive spherical Earth). */
export function greatCircleDistanceKm(
  a: AirportCoords,
  b: AirportCoords,
): number {
  const toRad = (deg: number) => (deg * Math.PI) / 180;
  const dLat = toRad(b.lat - a.lat);
  const dLon = toRad(b.lon - a.lon);
  const lat1 = toRad(a.lat);
  const lat2 = toRad(b.lat);
  const h =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(lat1) * Math.cos(lat2) * Math.sin(dLon / 2) ** 2;
  return 2 * EARTH_RADIUS_KM * Math.asin(Math.min(1, Math.sqrt(h)));
}

/** Distance between two IATA codes, or null if either is unknown. */
export function airportDistanceKm(
  origin: string,
  destination: string,
): number | null {
  const a = airportCoords(origin);
  const b = airportCoords(destination);
  if (!a || !b) return null;
  if (origin === destination) return 0;
  return greatCircleDistanceKm(a, b);
}

